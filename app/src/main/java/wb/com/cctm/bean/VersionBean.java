public class VersionBean {
    String V_ADDR;
    String V_NUMBER;
    String V_VERSION_CODE;

    public String getV_ADDR() {
        return V_ADDR;
    }

    public void setV_ADDR(String v_ADDR) {
        V_ADDR = v_ADDR;
    }

    public String getV_NUMBER() {
        return V_NUMBER;
    }

    public void setV_NUMBER(String v_NUMBER) {
        V_NUMBER = v_NUMBER;
    }

    public String getV_VERSION_CODE() {
        return V_VERSION_CODE;
    }

    public void setV_VERSION_CODE(String v_VERSION_CODE) {
        V_VERSION_CODE = v_VERSION_CODE;
    }
}
